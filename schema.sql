
-- LIBRARY HOUR MANAGEMENT SYSTEM SCHEMA


BEGIN EXECUTE IMMEDIATE 'DROP TABLE library_usage CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE library_hours CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE student CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE staff CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE library CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/



CREATE TABLE student (
    student_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(100) NOT NULL,
    email VARCHAR2(100) UNIQUE,
    department VARCHAR2(100)
);

CREATE TABLE staff (
    staff_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(100) NOT NULL,
    role VARCHAR2(50)
);


CREATE TABLE library (
    library_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    library_name VARCHAR2(100)
);


CREATE TABLE library_hours (
    assignment_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    student_id NUMBER NOT NULL,
    staff_id NUMBER NOT NULL,
    library_id NUMBER,
    assigned_hours NUMBER NOT NULL,
    assigned_datetime TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deadline_datetime TIMESTAMP,
    status VARCHAR2(30)
);

CREATE TABLE library_usage (
    log_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    assignment_id NUMBER NOT NULL,
    start_time TIMESTAMP,
    end_time TIMESTAMP,
    hours_used NUMBER
);


ALTER TABLE library_hours
ADD CONSTRAINT fk_lh_student
FOREIGN KEY (student_id)
REFERENCES student(student_id)
ON DELETE CASCADE;

ALTER TABLE library_hours
ADD CONSTRAINT fk_lh_staff
FOREIGN KEY (staff_id)
REFERENCES staff(staff_id);

ALTER TABLE library_hours
ADD CONSTRAINT fk_lh_library
FOREIGN KEY (library_id)
REFERENCES library(library_id);

ALTER TABLE library_usage
ADD CONSTRAINT fk_usage_assignment
FOREIGN KEY (assignment_id)
REFERENCES library_hours(assignment_id)
ON DELETE CASCADE;
